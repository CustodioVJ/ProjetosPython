def add(n1, n2):
    return n1 + n2

def subtract(n1, n2):
    return n1 - n2

def multiply(n1, n2):
    return n1 * n2

def divide(n1, n2):
    return n1 / n2

operations = {"+": add,
              "-": subtract,
              "*": multiply,
              "/": divide,
              }

user_ans = ""

while user_ans != "n":
    num1 = int(input("What's the first number?\n"))
    num2 = int(input("What's the second number?\n"))
    op = input("What do you want to do with this number?\n")
    for i in operations:
      print(i)
    op_output = operations[op]
    answer = op_output(num1, num2)
    print("pick one to get you answer:\n")

    print(f"{num1} {op} {num2} = {answer}\n")
    user_ans = input("Type 'y' to continue calculating or type 'n' to exit.: ")

